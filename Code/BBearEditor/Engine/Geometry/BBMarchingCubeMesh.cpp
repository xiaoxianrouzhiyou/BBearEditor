void BBMarchingCubeMesh::init(unsigned int *pNum, const QVector3D &unitWidth, const QVector3D &min, float fThreshold)
{
    if (m_bValidSurface)
    {
        deleteSurface();
    }

    m_fIsoLevel = fThreshold;
    m_Grid.m_nNum[0] = pNum[0];
    m_Grid.m_nNum[1] = pNum[1];
    m_Grid.m_nNum[2] = pNum[2];
    m_Grid.m_UnitWidth = unitWidth;
    m_Grid.m_Min = min;

    m_pCurrentMaterial->init("base", BB_PATH_RESOURCE_SHADER(base.vert), BB_PATH_RESOURCE_SHADER(base.frag));
    BBRenderableObject::init();

    BBDrawCall *pDrawCall = new BBDrawCall;
    pDrawCall->setMaterial(m_pCurrentMaterial);
    pDrawCall->setVBO(m_pVBO);
    pDrawCall->setEBO(m_pEBO, GL_TRIANGLES, m_nIndexCount, 0);
    appendDrawCall(pDrawCall);
}

bool BBMarchingCubeMesh::createMCMesh(float *pField)
{
    if (pField == nullptr)
        return false;
    // input density field
    m_pScalarField = pField;
    generateIsoSurface();
    return true;
}

void BBMarchingCubeMesh::generateIsoSurface()
{
    // used for computing index
    unsigned int nSlice0 = m_Grid.m_nNum[0] + 1;
    unsigned int nSlice1 = nSlice0 * (m_Grid.m_nNum[1] + 1);

    // generate isosurface
    for (unsigned int z = 0; z < m_Grid.m_nNum[2]; z++)
    {
        for (unsigned int y = 0; y < m_Grid.m_nNum[1]; y++)
        {
            for (unsigned int x = 0; x < m_Grid.m_nNum[0]; x++)
            {
                // Traverse each point of the density field and judge whether it exceeds the density threshold
                unsigned int nTableIndex = 0;
                if (m_pScalarField[z * nSlice1 + y * nSlice0 + x] < m_fIsoLevel)
                    nTableIndex |= 1;
                if (m_pScalarField[z * nSlice1 + (y + 1) * nSlice0 + x] < m_fIsoLevel)
                    nTableIndex |= 2;
                if (m_pScalarField[z * nSlice1 + (y + 1) * nSlice0 + (x + 1)] < m_fIsoLevel)
                    nTableIndex |= 4;
                if (m_pScalarField[z * nSlice1 + y * nSlice0 + (x + 1)] < m_fIsoLevel)
                    nTableIndex |= 8;
                if (m_pScalarField[(z + 1) * nSlice1 + y * nSlice0 + x] < m_fIsoLevel)
                    nTableIndex |= 16;
                if (m_pScalarField[(z + 1) * nSlice1 + (y + 1) * nSlice0 + x] < m_fIsoLevel)
                    nTableIndex |= 32;
                if (m_pScalarField[(z + 1) * nSlice1 + (y + 1) * nSlice0 + (x + 1)] < m_fIsoLevel)
                    nTableIndex |= 64;
                if (m_pScalarField[(z + 1) * nSlice1 + y * nSlice0 + (x + 1)] < m_fIsoLevel)
                    nTableIndex |= 128;

                if (m_EdgeTable[nTableIndex] != 0)
                {
                    /*
                           6--------7     *---5----*
                          /|       /|    /|       /|
                         / |      / |   9 |     10 |
                        /  |     /  |  /  4     /  6
                       2--------3   | *----1---*   |
                       |   |    |   | |   |    |   |
                       |   5----|---8 |   *---7|---*
                       |  /     |  /  0  /     2  /
                       | /      | /   | 8      | 11
                       |/       |/    |/       |/
                       1--------4     *---3----*

                       e.g.
                       v1 : 0000 0001
                       e0 e3 e8 : 0001 0000 1001 : 0x109

                     * */
                    // Priority min : 3, 0, 8
                    // The corresponding edge number of the point is obtained
                    // Take the edge number as the index and the point position as the value
                    if (m_EdgeTable[nTableIndex] & 8) // 0000 0000 1000 : 8 : 3
                    {
                        generateEdgeVertexMap(x, y, z, 3);
                    }
                    if (m_EdgeTable[nTableIndex] & 1)
                    {
                        generateEdgeVertexMap(x, y, z, 0);
                    }
                    if (m_EdgeTable[nTableIndex] & 256)
                    {
                        generateEdgeVertexMap(x, y, z, 8);
                    }
                    // Bottom & Right : 2, 11
                    if (x == m_Grid.m_nNum[0] - 1)
                    {
                        if (m_EdgeTable[nTableIndex] & 4)
                        {
                            generateEdgeVertexMap(x, y, z, 2);
                        }
                        if (m_EdgeTable[nTableIndex] & 2048)
                        {
                            generateEdgeVertexMap(x, y, z, 11);
                        }
                    }
                    // Top & Left : 1, 9
                    if (y == m_Grid.m_nNum[1] - 1)
                    {
                        if (m_EdgeTable[nTableIndex] & 2)
                        {
                            generateEdgeVertexMap(x, y, z, 1);
                        }
                        if (m_EdgeTable[nTableIndex] & 512)
                        {
                            generateEdgeVertexMap(x, y, z, 9);
                        }
                    }
                    //  Back & Left : 4, 7
                    if (z == m_Grid.m_nNum[2] - 1)
                    {
                        if (m_EdgeTable[nTableIndex] & 16)
                        {
                            generateEdgeVertexMap(x, y, z, 4);
                        }
                        if (m_EdgeTable[nTableIndex] & 128)
                        {
                            generateEdgeVertexMap(x, y, z, 7);
                        }
                    }
                    if ((x == m_Grid.m_nNum[0] - 1) && (y == m_Grid.m_nNum[1] - 1))
                    {
                        if (m_EdgeTable[nTableIndex] & 1024)
                        {
                            generateEdgeVertexMap(x, y, z, 10);
                        }
                    }
                    if ((x == m_Grid.m_nNum[0] - 1) && (z == m_Grid.m_nNum[2] - 1))
                    {
                        if (m_EdgeTable[nTableIndex] & 64)
                        {
                            generateEdgeVertexMap(x, y, z, 6);
                        }
                    }
                    if ((y == m_Grid.m_nNum[1] - 1) && (z == m_Grid.m_nNum[2] - 1))
                    {
                        if (m_EdgeTable[nTableIndex] & 32)
                        {
                            generateEdgeVertexMap(x, y, z, 5);
                        }
                    }

                    // generate triangle
                    for (unsigned int i = 0; m_TriangleTable[nTableIndex][i] != -1; i += 3)
                    {
                        BBMCTriangle triangle;
                        triangle.m_VertexID[0] = getEdgeID(x, y, z, m_TriangleTable[nTableIndex][i]);
                        triangle.m_VertexID[1] = getEdgeID(x, y, z, m_TriangleTable[nTableIndex][i + 1]);
                        triangle.m_VertexID[2] = getEdgeID(x, y, z, m_TriangleTable[nTableIndex][i + 2]);
                        m_TriangleVector.push_back(triangle);
                    }
                }
            }
        }
    }

    generateVBOAndEBO();
    m_bValidSurface = true;
}

void BBMarchingCubeMesh::generateEdgeVertexMap(unsigned int x, unsigned int y, unsigned int z, unsigned int nEdge)
{
    BBMCVertex v = computeIntersection(x, y, z, nEdge);
    unsigned int id = getEdgeID(x, y, z, nEdge);
    m_EVMap.insert(BBMCEVMap::value_type(id, v));
}

/**
 * @brief BBMarchingCubeMesh::computeIntersection                   Calculate equivalent points by interpolation
 * @param x
 * @param y
 * @param z
 * @param nEdge                                                     edge number
 */
BBMCVertex BBMarchingCubeMesh::computeIntersection(unsigned int x, unsigned int y, unsigned int z, unsigned int nEdge)
{
    // For a voxel edge with an intersection with the isosurface,
    // the intersection coordinates are represented by P,
    // P1 and P2 represent the coordinates of the two endpoints on the edge,
    // V1 and V2 represent the values on the two endpoints, and V represents the isosurface
    // P = P1 + (V - V1) * (P2 - P1) / (V2 - V1)
    // N is number
    QVector3D N1(x, y, z);
    QVector3D N2(x, y, z);
    switch (nEdge) {
    case 0:
        N2[1]++;
        break;
    case 1:
        N1[1]++;
        N2[0]++;
        N2[1]++;
        break;
    case 2:
        N1[0]++;
        N1[1]++;
        N2[0]++;
        break;
    case 3:
        N1[0]++;
        break;
    case 4:
        N1[2]++;
        N2[1]++;
        N2[2]++;
        break;
    case 5:
        N1[1]++;
        N1[2]++;
        N2[0]++;
        N2[1]++;
        N2[2]++;
        break;
    case 6:
        N1[0]++;
        N1[1]++;
        N1[2]++;
        N2[0]++;
        N2[2]++;
        break;
    case 7:
        N1[0]++;
        N1[2]++;
        N2[2]++;
        break;
    case 8:
        N2[2]++;
        break;
    case 9:
        N1[1]++;
        N2[1]++;
        N2[2]++;
        break;
    case 10:
        N1[0]++;
        N1[1]++;
        N2[0]++;
        N2[1]++;
        N2[2]++;
        break;
    case 11:
        N1[0]++;
        N2[0]++;
        N2[2]++;
        break;
    }

    QVector3D P1 = m_Grid.m_Min + N1 * m_Grid.m_UnitWidth;
    QVector3D P2 = m_Grid.m_Min + N2 * m_Grid.m_UnitWidth;

    // used for computing index
    unsigned int nSlice0 = m_Grid.m_nNum[0] + 1;
    unsigned int nSlice1 = nSlice0 * (m_Grid.m_nNum[1] + 1);

    float V1 = m_pScalarField[(int)(N1.z() * nSlice1 + N1.y() * nSlice0 + N1.x())];
    float V2 = m_pScalarField[(int)(N2.z() * nSlice1 + N2.y() * nSlice0 + N2.x())];

    float V = m_fIsoLevel;

    // The linear interpolation is calculated
    BBMCVertex intersection;
    intersection.m_nID = -1;
    intersection.m_Position = P1 + (V - V1) * (P2 - P1) / (V2 - V1);
    return intersection;
}

/**
 * @brief BBMarchingCubeMesh::getEdgeID
 * @param x
 * @param y
 * @param z
 * @param nEdge                                                 0~11
 * @return                                                      total
 */
unsigned int BBMarchingCubeMesh::getEdgeID(unsigned int x, unsigned int y, unsigned int z, unsigned int nEdge)
{
    switch (nEdge) {
    case 0:
        return getVertexID(x, y, z) + 1;
    case 1:
        return getVertexID(x, y + 1, z);
    case 2:
        return getVertexID(x + 1, y, z) + 1;
    case 3:
        return getVertexID(x, y, z);
    case 4:
        return getVertexID(x, y, z + 1) + 1;
    case 5:
        return getVertexID(x, y + 1, z + 1);
    case 6:
        return getVertexID(x + 1, y, z + 1) + 1;
    case 7:
        return getVertexID(x, y, z + 1);
    case 8:
        return getVertexID(x, y, z) + 2;
    case 9:
        return getVertexID(x, y + 1, z) + 2;
    case 10:
        return getVertexID(x + 1, y + 1, z) + 2;
    case 11:
        return getVertexID(x + 1, y, z) + 2;
    default:
        return -1;
    }
}

unsigned int BBMarchingCubeMesh::getVertexID(unsigned int x, unsigned int y, unsigned int z)
{
    // * 3 because of triangle
    return 3 * (z * (m_Grid.m_nNum[1] + 1) * (m_Grid.m_nNum[0] + 1) + y * (m_Grid.m_nNum[0] + 1) + x);
}

void BBMarchingCubeMesh::generateVBOAndEBO()
{
    BBMCEVMap::iterator mapIt = m_EVMap.begin();
    BBMCTriangleVector::iterator vecIt = m_TriangleVector.begin();
    unsigned int nVertexID = 0;

    // normalize id
    while (mapIt != m_EVMap.end())
    {
        (*mapIt).second.m_nID = nVertexID;
        nVertexID++;
        mapIt++;
    }

    if (nVertexID == 0)
        return;

    while (vecIt != m_TriangleVector.end())
    {
        for (unsigned int i = 0; i < 3; i++)
        {
            unsigned int newID = m_EVMap[(*vecIt).m_VertexID[i]].m_nID;
            (*vecIt).m_VertexID[i] = newID;
        }
        vecIt++;
    }

    mapIt = m_EVMap.begin();
    m_pVBO = new BBVertexBufferObject(nVertexID);
    for (unsigned int i = 0; i < nVertexID; i++, mapIt++)
    {
        m_pVBO->setPosition(i, (*mapIt).second.m_Position);
    }

    vecIt = m_TriangleVector.begin();
    m_nIndexCount = 3 * m_TriangleVector.size();
    m_pIndexes = new unsigned short[m_nIndexCount];
    for (int i = 0; i < m_nIndexCount; i += 3, vecIt++)
    {
        m_pIndexes[i] = (*vecIt).m_VertexID[0];
        m_pIndexes[i + 1] = (*vecIt).m_VertexID[1];
        m_pIndexes[i + 2] = (*vecIt).m_VertexID[2];
    }

    m_pVBO->computeNormal(m_pIndexes, m_nIndexCount);


    m_EVMap.clear();
    m_TriangleVector.clear();
}

void BBMarchingCubeMesh::deleteSurface()
{
    m_bValidSurface = false;
    m_pScalarField = nullptr;
    m_fIsoLevel = 0;
    m_Grid.m_nNum[0] = 0;
    m_Grid.m_nNum[1] = 0;
    m_Grid.m_nNum[2] = 0;
    m_Grid.m_UnitWidth = QVector3D(0, 0, 0);
    m_Grid.m_Min = QVector3D(0, 0, 0);
    m_EVMap.clear();
    m_TriangleVector.clear();
}
